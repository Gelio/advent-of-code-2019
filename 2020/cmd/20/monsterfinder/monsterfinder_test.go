package monsterfinder

import (
	"aoc-2020/cmd/20/positionhashmap"
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestGetMonstersCount(t *testing.T) {
	input := positionhashmap.FromLines(strings.Split(`.#.#..#.##...#.##..#####
###....#.#....#..#......
##.##.###.#.#..######...
###.#####...#.#####.#..#
##.#....#.##.####...#.##
...########.#....#####.#
....#..#...##..#.#.###..
.####...#..#.....#......
#..#.##..#..###.#.##....
#.####..#.####.#.#.###..
###.#.#...#.######.#..##
#.####....##..########.#
##..##.#...#...#.#.#.#..
...#..#..#.#.##..###.###
.#.#....#.##.#...###.##.
###.#...#..#.##.######..
.#.#.###.##.##.#..#.##..
.####.###.#...###.#..#.#
..#.#..#..#.#.#.####.###
#..####...#.#.#.###.###.
#####..#####...###....##
#.##..#..#...#..####...#
.#.###..##..##..####.##.
...###...##...#...#..###`, "\n"))

	monstersFound := GetMonstersCount(input)
	assert.Equal(t, 2, monstersFound)
}
